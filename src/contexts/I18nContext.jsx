import { createContext, useContext, useState, useEffect } from 'react'
import { useAuth } from './AuthContext'

const I18nContext = createContext(null)

export const I18nProvider = ({ children }) => {
  const { user, userProfile, updateProfile } = useAuth()

  const [language, setLanguage] = useState(() => {
    try { return localStorage.getItem('mizan.lang') || 'ar' } catch { return 'ar' }
  })

  // Sync from user profile on login
  useEffect(() => {
    if (userProfile?.language) setLanguage(userProfile.language)
  }, [userProfile])

  // Apply lang/dir to DOM and persist to localStorage
  useEffect(() => {
    document.documentElement.setAttribute('lang', language)
    document.documentElement.setAttribute('dir', language === 'ar' ? 'rtl' : 'ltr')
    try { localStorage.setItem('mizan.lang', language) } catch {}
  }, [language])

  const changeLanguage = async (lang) => {
    setLanguage(lang)
    if (user) await updateProfile({ language: lang })
  }

  const t = (key) => translations[language]?.[key] || translations['en'][key] || key

  return (
    <I18nContext.Provider value={{ language, changeLanguage, t }}>
      {children}
    </I18nContext.Provider>
  )
}

export const useI18n = () => {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be inside I18nProvider')
  return ctx
}

